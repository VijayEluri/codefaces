package org.codefaces.core.services.github.dtos;

public class GitHubResourceDto {
	private String name;
	private String sha;
	private String mode;
	private String type;
	
	public String getName() {
		return name;
	}
	public String getSha() {
		return sha;
	}
	public String getMode() {
		return mode;
	}
	public String getType() {
		return type;
	}
}
